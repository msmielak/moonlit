# Convert phase angle (degrees) to fraction of disc illuminated, which is the
# x-axis used by the spline (matches the moonIllum$fraction output of suncalc)
.disc_illuminated_data <- (1 + cos(.phase_angle_data * pi / 180)) / 2

#' @keywords internal
.brightness_spline <- stats::splinefun(
  x      = .disc_illuminated_data,
  y      = .phase_brightness_data,
  method = "fmm",
  ties   = mean
)

